package PatternPrograms;

public class number_pattern_7
{
    static void main(String[] args) {
        int i ,j;

        i = 1;
        // while loop
        while (i<=5){
            j=1;
            while(j<=5){
                System.out.print(j);
                j++;

            }
            System.out.println();
            i++;


        }
    }

}
