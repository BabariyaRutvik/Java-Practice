package PatternPrograms;

public class reverse_same_number_pattern
{
    static void main() {
        for (int i = 5; i>=1 ; i--){
            for (int j = 1 ; j<=5 ; j++){
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
