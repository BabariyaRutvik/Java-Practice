package PatternPrograms;

public class Reverse_number_pattern_8 {
    static void main(String[] args) {
        int i ,j;
        for (i = 5 ; i>=1 ; i--){
            for (j = 5 ; j>=1 ; j--){
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
